        [Authorize(Roles = "Administrator")]
        public ActionResult ListRoles()
        {
            // Action to view all of the current roles in our db.

            // build a list of RoleViewModels
            var db = new ApplicationDbContext();
            var model = new List<RoleViewModel>();

            foreach (var role in db.Roles)
                model.Add(new RoleViewModel { RoleId = role.Id, RoleName = role.Name });

            return View(model);
        }


        // GET: /Account/AssignUserRole
        [Authorize(Roles = "Administrator")]
        public ActionResult AssignUserRole(string roleId)
        {
            // CHECK YOUR INPUTS!!
            if (roleId == null)
            {
                return RedirectToAction("ListRoles");
            }   

            // quick error checking
            ViewBag.Message = (string)TempData["ListError"];

            // Build the UserRoleViewModel
            var model = new UserRoleViewModel();

            var db = new ApplicationDbContext();
            var role = db.Roles.Find(roleId);

            model.RoleName = role.Name;
            model.RoleId = role.Id;


            // find all of the people NOT in the role, so we could potentally add them
            var userList = db.Users.Where(u => !u.Roles.Any(r => r.RoleId == roleId));

            // --- to simplify 1st order user list, then add to multiselectlist. ---
            // var orderedList = userList.OrderBy(_user => _user.UserName);
            // var multiSelect = new MultiSelectList(orderedList, "Id", "UserName");

            // now put userlist as the models multiselectlist
            model.Users = new MultiSelectList(userList.OrderBy(u => u.UserName), "Id", "UserName");

            return View(model);
        }


        // POST: /Account/AssigneUserRole
        [HttpPost]
        [Authorize(Roles = "Administrator")]
        [ValidateAntiForgeryToken]
        public ActionResult AssignUserRole(UserRoleViewModel model)
        {

            // check model is valid
            if (!ModelState.IsValid)
            {
                return View("Error");
            }

            // check that the selectuser list isn't empty
            if (model.SelectedUsers == null)
            {
                TempData["ListError"] = "At least one user should be selected.";
                return RedirectToAction("AssignUserRole", new { roleId = model.RoleId });
            }

            // now is time to add the selected users to the current role.
            foreach (var userId in model.SelectedUsers)
            {
                UserManager.AddToRole(userId, model.RoleName);
            }


            return RedirectToAction("ListRoles");
        }


        // GET: /Account/UnassignUserRole
        [Authorize(Roles = "Administrator")]
        public ActionResult UnassignUserRole(string roleId)
        {
            // CHECK YOUR INPUTS!!
            if (roleId == null)
            {
                return RedirectToAction("ListRoles");
            }

            // quick error checking
            ViewBag.Message = (string)TempData["ListError"];


            // Build a UserRoleModelView so that we can unselect users from a role
            var db = new ApplicationDbContext();
            var model = new UserRoleViewModel();

            var role = db.Roles.Find(roleId);
            model.RoleId = role.Id;
            model.RoleName = role.Name;

            // build list of users in a specific role
            var userList = db.Users.Where(u => u.Roles.Any(r => r.RoleId == roleId));


            // add the new userlist as a multiselectlist to the model, don't forget to order the userlist.
            model.Users = new MultiSelectList(userList.OrderBy(us => us.UserName), "Id", "UserName");

            return View(model);
        }


        // POST: /Account/UnassignUserRole
        [HttpPost]
        [Authorize(Roles = "Administrator")]
        [ValidateAntiForgeryToken]
        public ActionResult UnassignUserRole(UserRoleViewModel model)
        {
            // check model is valid
            if (!ModelState.IsValid)
            {
                return View("Error");
            }

            // check that the selectuser list isn't empty
            if (model.SelectedUsers == null)
            {
                TempData["ListError"] = "At least one user should be selected.";
                return RedirectToAction("UnassignUserRole", new { roleId = model.RoleId });
            }


            // time to remove users from the role
            foreach (var userID in model.SelectedUsers)
            {
                UserManager.RemoveFromRole(userID, model.RoleName);
            }


            return RedirectToAction("ListRoles");
        }
